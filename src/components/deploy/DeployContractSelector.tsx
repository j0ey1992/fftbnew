interface DeployContractSelectorProps {
  onSelectContractType: (contractType: string) => void
}

export function DeployContractSelector({ onSelectContractType }: DeployContractSelectorProps) {
  const [activeTab, setActiveTab] = useState('tokens')
  
  const tabs = [
    { id: 'tokens', label: 'Tokens', icon: <TokenIcon /> },
    { id: 'nfts', label: 'NFTs', icon: <NftIcon /> },
    { id: 'personal', label: 'Personal', icon: <PersonalIcon /> },
    { id: 'multi', label: 'Multi Projects', icon: <MultiProjectIcon /> },
  ]
  
  // Get contract configs by category
  const tokenContracts = useMemo(() => getContractConfigsByCategory('tokens'), [])
  const nftContracts = useMemo(() => getContractConfigsByCategory('nfts'), [])
  const personalContracts = useMemo(() => getContractConfigsByCategory('personal'), [])
  const multiContracts = useMemo(() => getContractConfigsByCategory('multi'), [])
  
  // Animation variants for tab panels
  const tabPanelVariants = {
    hidden: { opacity: 0, y: 10 },
    visible: { 
      opacity: 1, 
      y: 0,
      transition: {
        duration: 0.4,
        staggerChildren: 0.1,
        delayChildren: 0.1
      }
    },
    exit: { 
      opacity: 0,
      y: -10,
      transition: { duration: 0.2 }
    }
  };
  
  const cardVariants = {
    hidden: { opacity: 0, y: 20 },
    visible: { 
      opacity: 1, 
      y: 0,
      transition: { duration: 0.3 }
    }
  };
  
  // Function to render contract cards with animations
  const renderContractCards = (contracts: ContractConfig[]) => (
    <motion.div 
      className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-5"
      variants={tabPanelVariants}
      initial="hidden"
      animate="visible"
      exit="exit"
    >
      {contracts.map((contract: ContractConfig, index: number) => (
        <motion.div key={contract.id} variants={cardVariants} custom={index}>
          <DeployContractCard
            title={contract.name}
            description={contract.description}
            icon={getContractIcon(contract.icon)}
            onClick={() => onSelectContractType(contract.id)}
            accentColor={contract.accentColor}
            comingSoon={contract.comingSoon}
          />
        </motion.div>
      ))}
    </motion.div>
  );
  
  return (
    <div className="relative">
      {/* Enhanced tab bar with subtle background */}
      <div className="relative mb-8">
        <div className="absolute inset-0 bg-gradient-to-r from-blue-900/10 to-indigo-900/10 rounded-xl -z-10"></div>
        <TabBar 
          tabs={tabs} 
          activeTab={activeTab}
          onChange={setActiveTab}
          variant="pill"
          className="relative z-10"
        />
      </div>
      
      {/* Tab panels with animations */}
      <AnimatePresence mode="wait">
        <TabPanel id="tokens" activeTab={activeTab} key="tokens">
          {renderContractCards(tokenContracts)}
        </TabPanel>
        
        <TabPanel id="nfts" activeTab={activeTab} key="nfts">
          {renderContractCards(nftContracts)}
        </TabPanel>
        
        <TabPanel id="personal" activeTab={activeTab} key="personal">
          {renderContractCards(personalContracts)}
        </TabPanel>
        
        <TabPanel id="multi" activeTab={activeTab} key="multi">
          {renderContractCards(multiContracts)}
        </TabPanel>
      </AnimatePresence>
      
      {/* Decorative background elements */}
      <div className="absolute top-1/2 right-0 w-64 h-64 bg-blue-500/5 rounded-full blur-3xl -z-10 transform translate-x-1/2"></div>
      <div className="absolute bottom-0 left-0 w-96 h-96 bg-indigo-500/5 rounded-full blur-3xl -z-10 transform -translate-x-1/3"></div>
    </div>
  )
}
